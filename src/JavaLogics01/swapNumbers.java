package JavaLogics01;

public class swapNumbers {

public static void main(String[] args) {
		
		
		int num1= 10; 
		int num2=5; 
		
		//output => num1=5, num2=10
		
		 num1 = num1-num2; 
		 num2 = num1+num1; 
		 
		 System.out.println("num1: " + num1);
		 System.out.println("num2: " + num2);
		
		
	}
}
